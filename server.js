const express = require('express')
const bodyParser = require('body-parser')
const rootRoute = require('./routes/rootRoute')
const registerRoute = require('./routes/registerRoute')
const loginRoute = require('./routes/loginRoutes')
const addTransactions = require('./routes/transaction/addTransactions')
const getTransactions = require('./routes/transaction/getTransactions')
const editTransactions = require('./routes/transaction/editTransactions')
const deleteTransaction = require('./routes/transaction/deleteTransaction')
const addStores = require('./routes/stores/addStores')
const getStores = require('./routes/stores/getStores')
const editStores = require('./routes/stores/editStores')
const deleteStores = require('./routes/stores/deleteStores')
const addBooks = require('./routes/books/addBooks')
const getBooks = require('./routes/books/getBooks')
const editBooks = require('./routes/books/editBooks')
const deleteBooks = require('./routes/books/deleteBooks')

const app = express()
app.use(bodyParser.json())
app.use(rootRoute)
app.use(registerRoute)
app.use(loginRoute)
app.use(addTransactions)
app.use(getTransactions)
app.use(deleteTransaction)
app.use(editTransactions)
app.use(addStores)
app.use(getStores)
app.use(editStores)
app.use(deleteStores)
app.use(addBooks)
app.use(getBooks)
app.use(editBooks)
app.use(deleteBooks)

const port = 3000
app.listen(port, () => {
  console.log(`Backend app is running in http://localhost:${port}`);
})
